print("Enter number one")
number1 = float(input())

print("Enter number two")
number2 = float(input())

print("Enter operation")
operation = input()

if operation == "+":
    result1 = number1+ number2
    print(result1)
elif operation == "-":
    result1 = number1 - number2
    print(result1)
elif operation == "*":
    result1 = number1 * number2
    print(result1)
elif operation == "/":
    result1 = number1 / number2
    print(result1)
else:
    print("Unknown operation")

